package com.abrilcodes.pos.backend.model;
import jakarta.persistence.*;
import lombok.Data;

@Data
@Entity
@Table(name = "roles")
public class Role {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "role_id")
    private Byte roleId; // tinyint(4)
    
    @Column(name = "role_name")
    private String roleName;
}